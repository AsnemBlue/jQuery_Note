    ID VARCHAR2(30) NOT NULL,
    PW VARCHAR2(30) NOT NULL,
    NAME VARCHAR2(100) NOT NULL,
    BIRTH DATE,
    GENDER VARCHAR2(3) NOT NULL,
    EMAIL VARCHAR2(100) NOT NULL,
    PHONE VARCHAR2(100),
    ADDRESS VARCHAR2(100),
    PRIMARY KEY(ID)
);
